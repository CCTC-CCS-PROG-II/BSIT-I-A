public class NAIRY_ACTIVITY_I{
   
    public static void main(String[]args){
        
        int q = 15;
        int w = 25;
        int e = 45;
        
        int sum = q + w + e;
        int diff = q - w - e;
        int prod = q * w * e;
        int quot = q / w / e;

        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + diff);
        System.out.println("Product: " + prod);
        System.out.println("Quotient: " + quot);

    }
}
